package vn.edu.huflit.yogadaily;

public class PopularPoses {
    String name;
    int image;

    public PopularPoses(String name, int image){
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
